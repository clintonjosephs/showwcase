class University {
  country: string;
  name: string;
  domain: string;
  web_page: string;

  constructor(country: string, name: string, domain: string, web_page: string) {
    this.country = country;
    this.name = name;
    this.domain = domain;
    this.web_page = web_page;
  }
}

export default University;
